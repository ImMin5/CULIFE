package com.team.culife.vo;

public class AuthorVO {
	private int no;
	private int member_no;
	private String author;
	private String sns_link;
	private String author_thumbnail;
	private int author_status;
	private String create_date;
	private int debut_year;
	private String msg;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getMember_no() {
		return member_no;
	}
	public void setMember_no(int member_no) {
		this.member_no = member_no;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getSns_link() {
		return sns_link;
	}
	public void setSns_link(String sns_link) {
		this.sns_link = sns_link;
	}
	public String getAuthor_thumbnail() {
		return author_thumbnail;
	}
	public void setAuthor_thumbnail(String author_thumbnail) {
		this.author_thumbnail = author_thumbnail;
	}
	public int getAuthor_status() {
		return author_status;
	}
	public void setAuthor_status(int author_status) {
		this.author_status = author_status;
	}
	public String getCreate_date() {
		return create_date;
	}
	public void setCreate_date(String create_date) {
		this.create_date = create_date;
	}
	public int getDebut_year() {
		return debut_year;
	}
	public void setDebut_year(int debut_year) {
		this.debut_year = debut_year;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	

}
